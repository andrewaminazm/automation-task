import org.openqa.selenium.Keys
public class SearchKeywords {
	@Keyword
	def searchFor(String query) {
		WebUI.setText(findTestObject('Page_Home Page/input_Search_q'), query)
		WebUI.sendKeys(findTestObject('Page_Home Page/input_Search_q'), Keys.chord(Keys.ENTER))
	}

	@Keyword
	def verifySearchResults(List<String> expectedItems) {
		for (String item : expectedItems) {
			TestObject itemObj = findTestObject("Page_Search results for Nike/a_" + item)
			WebUI.verifyElementVisible(itemObj)
		}
	}
}
